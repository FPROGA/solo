async function run() {
  const client = createPgClient();
  await client.connect();

  const subjectId = 1;
  const classNum = 2;
  const title1 = 'Итоговый тест по русскому языку (вариант 1)';
  const title2 = 'Итоговый тест по русскому языку (вариант 2)';

  const id1 = await ensureTest(client, {
    subjectId,
    classNum,
    title: title1,
    isDemo: false,
  });
  const id2 = await ensureTest(client, {
    subjectId,
    classNum,
    title: title2,
    isDemo: false,
  });

  const v1 = variant1.map(([q, a, b, c, d, ans]) => ({
    question_text: q,
    option_a: a,
    option_b: b,
    option_c: c,
    option_d: d,
    correct_answer: ans,
  }));
  const v2 = variant2.map(([q, a, b, c, d, ans]) => ({
    question_text: q,
    option_a: a,
    option_b: b,
    option_c: c,
    option_d: d,
    correct_answer: ans,
  }));

  const r1 = await insertQuestions(client, id1, v1);
  const r2 = await insertQuestions(client, id2, v2);

  console.log(
    JSON.stringify(
      {
        subjectId,
        classNum,
        testVariant1Id: id1,
        testVariant2Id: id2,
        variant1: r1,
        variant2: r2,
      },
      null,
      2,
    ),
  );
  await client.end();
}

run().catch((e) => {
  console.error(e);
  process.exit(1);
});
